package oopWithNLayeredApp.entity;

public class Object {
	private int id;
	private String name;
	private String kind;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getKind() {
		return kind;
	}
}
